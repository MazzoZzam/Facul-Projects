#include <stdio.h>
#include <stdlib.h>
float som(float val1, float val2) {
	return val1 + val2;
}

float sub(float val1, float val2) {
	return val1 - val2;
}

float mult(float val1, float val2) {
	return val1 * val2;
}

float divi(float val1, float val2) {
	return val1 / val2;
}

int rest(int val1, int val2) {
	return val1 % val2;
}

